void solve() {
	int N, M, P;
	cin >> N >> M >> P;

	vector<int> md(N);
	vector<int> sd(M);

	for (auto &i : md)
		cin >> i;

	for (auto &j : sd)
		cin >> j;

	sort(md.begin(), md.end());
	sort(sd.begin(), sd.end());

	long long ans = 0;

	for (int i=0;i<N;i++) {
		int j;
		for (j=0;j<M;j++) {
			int temp = md[i] + sd[j];
			if (temp >= P) {
				break;
			} else {
				ans += temp;
			}
		}

		ans += (M - j)  * P;
		debug(ans);
	}

	cout << ans << endl;
}

